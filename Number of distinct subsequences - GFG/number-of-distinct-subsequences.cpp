class Solution{
  public:	
	int distinctSubsequences(string s)
	{
	    long endsWith[26] = {}, mod = 1e9 + 7;
        for (char c : s)
            endsWith[c - 'a'] = accumulate(begin(endsWith), end(endsWith), 1L) % mod;
        return accumulate(begin(endsWith), end(endsWith), 0L) % mod + 1;
	}
};

